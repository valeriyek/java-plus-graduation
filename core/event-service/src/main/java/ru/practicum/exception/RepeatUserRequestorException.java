package ru.practicum.exception;
/**
 * Исключение, сигнализирующее о повторной заявке на участие.
 * <p>Выбрасывается, если пользователь пытается создать заявку на событие,
 * для которого у него уже есть активная заявка (в любом статусе,
 * кроме отклонённой или отменённой).</p>
 *
 * <p>Примеры использования:</p>
 * <ul>
 *   <li>повторная подача заявки на то же событие;</li>
 *   <li>создание новой заявки, когда уже существует подтверждённая;</li>
 *   <li>дублирование запроса при конкурентных обращениях.</li>
 * </ul>
 */
public class RepeatUserRequestorException extends RuntimeException {
    public RepeatUserRequestorException(String message) {
        super(message);
    }
}